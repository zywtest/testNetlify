/**
 * Generate the target date time
 * @param {String} key A Date value generated by JS
 * @returns {String} The fake date and time by the given parameter with format yyyy-mm-dd hh:mm:ss
 */
function generateTargetDate(key) {
  var targetdate = new Date(key);
  var datetime =  targetdate.toLocaleDateString('en-CA', {year: 'numeric', month: '2-digit', day: '2-digit',}) + ' ' +
                  targetdate.toLocaleTimeString(undefined, {hour12: false, hour: '2-digit', minute: '2-digit', second: '2-digit'});
  return datetime;
}
